def cornerCaseBR(i, j):
  """
  Returns the sum of the adjacent elements of the point on the lower right of the grid
  """
  sum = grid[i][j-1] + grid[i-1][j-1] + grid[i-1][j]
  return sum

def one(n):
  """
  Determines the new value of a non zero point 
  """
  if sumNear(n) < 2:
    return 0
  elif sumNear(n) > 3:
    return 0
  else:
    return 1
def zero(n):
  """
  Determines the new value of a zero point 
  """
  if sumNear(n) == 3:
    return 1
  else:
    return 0

def rules(n):
  """
  Determines if a coordinate is zero or not and returns an action based on its value
  """
  moves = coordinates[n]
  i, j = [moves[0], moves[1]]
  if grid[i][j] == 0:
    return zero(n)
  else:
    return one(n)

def changeGrid(grid,actions):
  """
  Updates the grid based on the rules of Game of one
  """
  for action in actions:
    if action[1] == 0:
      grid[action[0][0]][action[0][1]] = 0
    else:
      grid[action[0][0]][action[0][1]] = 1

