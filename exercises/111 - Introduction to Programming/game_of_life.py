def sumNear(n):
  """
  Determines the number of live neightbors of a point; that is, the sum of the adjacent elements
  """
  moves = coordinates[n]
  i, j = [moves[0], moves[1]]
  sum = 0
  if 1 <= i <= 19 and 1 <= j <= 19:
    sum += middleCase(i, j)
  elif i == 0 and 1 <= j <= 19:
    sum += topCase(i, j)
  elif i == 20 and 1 <= j <= 19:
    sum += bottomCase(i, j)
  elif 1 <= i <= 19 and j == 20:
    sum += rightCase(i, j)
  elif 1 <= i <= 19 and j == 0:
    sum += leftCase(i, j)
  elif i == 0 and j == 0:
    sum += cornerCaseTL(i, j)
  elif i == 0 and j == 20:
    sum += cornerCaseTR(i, j)
  elif i == 20 and j == 0:
    sum += cornerCaseBL(i, j)
  elif i == 20 and j == 20:
    sum += cornerCaseBR(i, j)
  return sum

